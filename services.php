			-&nbsp;Installer un système de sauvegarde de données sur un site FTP de sauvegarde géré par la M2L ;<br>
			-&nbsp;Effectuer les dernières mises à jour systèmes et à paramétrer leur automatisation ;<br>
			-&nbsp;Paramétrer les noms des postes selon les règles de gestion suivantes :<br>
