			</p>
			<h4>Impressions</h4><br>	
			<p>	
				Les ligues disposent de la possibilité d'imprimer en volume et en qualité d'imprimerie sur des ressources d'impression numériques connectées situées dans le local reprographie du rez-de-chaussée dont l'usage fait l'objet d'une facturation à prix coûtant.<br>
				Un système de comptage situé sur le serveur d'impression permet au CROSL d’effectuer une facturation mensuelle auprès des ligues :<br>
				- une photocopieuse noir et blanc à 70 pages/minute avec différents dispositifs de finition ;<br>
				- une imprimante Laser couleur A4/A3 à encre solide à 25 pages/minute ;<br>
				- un traceur A2 (1 page / minute) utilisé pour les affiches et banderoles.<br><br>
	
			</p>
			<h4>Serveur FTP documentaire</h4><br>	
			<p>	
				La M2L met à disposition des ligues un serveur FTP documentaire intranet/internet regroupant des textes légaux, des modèles de dossiers, de statuts, des programmes de formation (...) compilés par le CROSL. <br><br>

			</p>
